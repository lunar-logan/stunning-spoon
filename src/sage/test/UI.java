    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here: Export to Workbench
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File(lastBrowseLocation));
        fc.setFileFilter(new FileNameExtensionFilter("JSON File", ".json"));
        int res = fc.showSaveDialog(this);
        if (res == JFileChooser.APPROVE_OPTION) {
            String fileName = tMap.keySet().stream().findFirst().get();
            ArrayList<SPOTriplet> triples = tMap.get(fileName);

            File file = new File(fc.getSelectedFile() + ".json");
            log("\nSaving in the file " + file.toString());
            try {
                JSONObject jsonObject = new JSONObject().put("desc", fileName);
                JSONArray array = new JSONArray();
                triples.forEach(t -> {
                    JsonObject tr = t.getAsJsonObject();
                    try {
                        JSONObject o = new JSONObject()
                                .put("sub", tr.getString("sub"))
                                .put("pre", tr.getString("pre"))
                                .put("obj", tr.getString("obj"));
                        array.put(o);
                    } catch (JSONException e) {
                        e.printStackTrace();
                    }
                });
                jsonObject.put("triplets", array);
                FileOutputStream fout = new FileOutputStream(file);
                fout.write(jsonObject.toString().getBytes());
                fout.close();
            } catch (JSONException | IOException e) {
                e.printStackTrace();
            }
        }
    }

