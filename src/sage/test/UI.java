    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here: extract
        // Get the doc path
        ArrayList<String> docs = new ArrayList<>();
        try {
            docs.add(jTextField1.getText());
        } catch (NullPointerException ex) {
            ex.printStackTrace();
        }

        String vocab = null;
        try {
            vocab = jTextField2.getText();
        } catch (NullPointerException nex) {
            nex.printStackTrace();
        }

        final String vocabFile = vocab;

        if (docs.size() > 0 && vocabFile != null) {
            log("Beginning to extract triples...");
            new SwingWorker<Void, Void>() {
                @Override
                protected Void doInBackground() throws Exception {
                    Framework exF = new Framework(vocabFile, docs);
                    exF.setOnPostComplete(triplesMap -> {
                        tMap = triplesMap;
                        return null;
                    });
                    exF.run();
                    return null;
                }

                @Override
                protected void done() {
                    log("\nTriples extraction completed!");
                    if (tMap != null) {
                        String file = tMap.keySet().stream().findFirst().get();
                        ArrayList<SPOTriplet> triples = tMap.get(file);

                        Object[][] data = new Object[triples.size()][3];
                        for (int i = 0; i < triples.size(); i++) {
                            SPOTriplet t = triples.get(i);
                            data[i][0] = Sentence.listToString(t.getSubject());
                            data[i][1] = Sentence.listToString(t.getPredicate());
                            data[i][2] = Sentence.listToString(t.getObject());
                        }

                        String[] headers = {"Subject", "Predicate", "Object"};

                        System.out.println(tMap);

                        // Show the file in the table
                        jTable1.setModel(new DefaultTableModel(data, headers));
                    }
                }
            }.execute();
        }
    }

